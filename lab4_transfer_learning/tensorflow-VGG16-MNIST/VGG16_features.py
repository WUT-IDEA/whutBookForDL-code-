if __name__ == '__main__':
    sess = tf.Session()
    imgs = tf.placeholder(tf.float32, [None, 28, 28, 3])
    labels = tf.placeholder(tf.int64, [None, ])
    vgg = vgg16(imgs, labels, '../VGG16/vgg16_weights.npz', sess)
    sess.run(tf.global_variables_initializer())

    accuracy = []

    mnist = input_data.read_data_sets('./MNIST_data')
    batch_size = 1000
    # 获取VGG网络中的某层网络输出作为特征
    for _ in xrange(50):
        for i in xrange(mnist.train.num_examples // batch_size):
            batch_images, batch_label = mnist.train.next_batch(batch_size)
            batch_images = vgg.from1D_to_3D_3ch(batch_images) * 255
            sess.run(vgg.optimizer_op, feed_dict={vgg.imgs: batch_images, vgg.labels: batch_label})
        acc = vgg.accuracy_op.eval(feed_dict={vgg.imgs: vgg.from1D_to_3D_3ch(mnist.test.images) * 255,
                                              vgg.labels: mnist.test.labels}, session=sess)
        print('%.3f' % (acc))
        accuracy.append(acc)
    np.save('VGG16_layer_%s' % (FLAGS.VGG_LAYER_LIMIT), accuracy)
